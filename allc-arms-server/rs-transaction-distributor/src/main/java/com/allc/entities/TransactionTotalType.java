public class TransactionTotalType {
	private Integer transactionTotalTypeID;
	private String transactionTotalTypeCode;
	private String name;

	/**
	 * @return the transactionTotalTypeID
	 */
	public Integer getTransactionTotalTypeID() {
		return transactionTotalTypeID;
	}

	/**
	 * @param transactionTotalTypeID
	 *            the transactionTotalTypeID to set
	 */
	public void setTransactionTotalTypeID(Integer transactionTotalTypeID) {
		this.transactionTotalTypeID = transactionTotalTypeID;
	}

	/**
	 * @return the transactionTotalTypeCode
	 */
	public String getTransactionTotalTypeCode() {
		return transactionTotalTypeCode;
	}

	/**
	 * @param transactionTotalTypeCode
	 *            the transactionTotalTypeCode to set
	 */
	public void setTransactionTotalTypeCode(String transactionTotalTypeCode) {
		this.transactionTotalTypeCode = transactionTotalTypeCode;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

}
